package sources.analogique;

/**
 * Enum représentant différents types de sources analogiques.
 */
public enum SourceAnalogiqueType {
    /**
     * Type de codage Non-Return-to-Zero (NRZ).
     */
    NRZ,
    /**
     * Type de codage Non-Return-to-Zero Inversé (NRZT).
     */
    NRZT,
    /**
     * Type de codage Return-to-Zero (RZ).
     */
    RZ
}